#include <ft_sh2.h>

void		ft_exit(char *str)
{
	int		nb;

	if (str)
	{
		nb = ft_atoi(str);
		if (nb > 0 && nb <= 255)
			exit(nb);
		else
			exit(1);
	}
	else
		exit(0);
}
